    "# Cell 3: SRCNN Model definition\n",
    "class SRCNN(nn.Module):\n",
    "    def __init__(self):\n",
    "        super(SRCNN, self).__init__()\n",
    "        self.layer1 = nn.Conv2d(1, 64, kernel_size=9, padding=4)\n",
    "        self.layer2 = nn.Conv2d(64, 32, kernel_size=5, padding=2)\n",
    "        self.layer3 = nn.Conv2d(32, 1, kernel_size=5, padding=2)\n",
    "        self.relu = nn.ReLU(inplace=True)\n",
    "\n",
    "    def forward(self, x):\n",
    "        x = self.relu(self.layer1(x))\n",
    "        x = self.relu(self.layer2(x))\n",
    "        x = self.layer3(x)\n",
    "        return x\n",
    "\n",
    "# %%\n",
    "# Cell 4: EDSR Model definition\n",
    "class ResidualBlock(nn.Module):\n",
    "    def __init__(self, channels):\n",
    "        super().__init__()\n",
    "        self.conv1 = nn.Conv2d(channels, channels, 3, padding=1)\n",
    "        self.relu = nn.ReLU(inplace=True)\n",
    "        self.conv2 = nn.Conv2d(channels, channels, 3, padding=1)\n",
    "\n",
    "    def forward(self, x):\n",
    "        residual = x\n",
    "        out = self.relu(self.conv1(x))\n",
    "        out = self.conv2(out)\n",
    "        out += residual\n",
    "        return out\n",
    "\n",
    "class EDSR(nn.Module):\n",
    "    def __init__(self, num_blocks=8, channels=64, scale=2):\n",
    "        super().__init__()\n",
    "        self.conv1 = nn.Conv2d(1, channels, 3, padding=1)\n",
    "        self.res_blocks = nn.Sequential(*[ResidualBlock(channels) for _ in range(num_blocks)])\n",
    "        self.conv2 = nn.Conv2d(channels, channels, 3, padding=1)\n",
    "        # Upsampling\n",
    "        self.upsample = nn.Sequential(\n",
    "            nn.Conv2d(channels, channels * scale * scale, 3, padding=1),\n",
    "            nn.PixelShuffle(scale),\n",
    "            nn.Conv2d(channels, 1, 3, padding=1)\n",
    "        )\n",
    "\n",
    "    def forward(self, x):\n",
    "        x = self.conv1(x)\n",
    "        x = self.res_blocks(x)\n",
    "        x = self.conv2(x)\n",
    "        x = self.upsample(x)\n",
    "        return x\n",
