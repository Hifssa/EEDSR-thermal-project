    "# Cell 2: Dataset definition\n",
    "class InfraredDataset(Dataset):\n",
    "    def __init__(self, folder, scale=4, crop_size=512):\n",
    "        self.all_files = [os.path.join(folder, f) for f in os.listdir(folder)\n",
    "                      if f.lower().endswith(('.png', '.jpg', '.jpeg', '.bmp'))]\n",
    "        self.files = [f for f in self.all_files if cv2.imread(f, cv2.IMREAD_GRAYSCALE) is not None]\n",
    "        self.scale = scale\n",
    "        self.crop_size = crop_size\n",
    "        self.transform = ToTensor()\n",
    "\n",
    "        # Check for invalid images\n",
    "        for f in self.all_files:\n",
    "            if f not in self.files:\n",
    "                print(f\"Skipping invalid image: {f}\")\n",
    "\n",
    "    def __len__(self):\n",
    "        return len(self.files)\n",
    "\n",
    "    def __getitem__(self, idx):\n",
    "        img = cv2.imread(self.files[idx], cv2.IMREAD_GRAYSCALE)\n",
    "        if img is None:\n",
    "            # Just in case, skip and pick another image\n",
    "            idx = (idx + 1) % len(self.files)\n",
    "            img = cv2.imread(self.files[idx], cv2.IMREAD_GRAYSCALE)\n",
    "        \n",
    "        # Center crop or resize\n",
    "        h, w = img.shape\n",
    "        if h < self.crop_size or w < self.crop_size:\n",
    "            img = cv2.resize(img, (self.crop_size, self.crop_size), interpolation=cv2.INTER_CUBIC)\n",
    "        else:\n",
    "            top = (h - self.crop_size) // 2\n",
    "            left = (w - self.crop_size) // 2\n",
    "            img = img[top:top+self.crop_size, left:left+self.crop_size]\n",
    "        \n",
    "        hr = self.transform(img)\n",
    "        \n",
    "        # Downsample for LR\n",
    "        lr_img = cv2.resize(img, (self.crop_size // self.scale, self.crop_size // self.scale), \n",
    "                           interpolation=cv2.INTER_CUBIC)\n",
    "        lr_img = cv2.resize(lr_img, (self.crop_size, self.crop_size), \n",
    "                           interpolation=cv2.INTER_CUBIC)\n",
    "        lr = self.transform(lr_img)\n",
    "        \n",
    "        return lr, hr\n",
