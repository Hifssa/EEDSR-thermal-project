# Training
# ------------------------------
def train():
    os.makedirs(SAVE_DIR, exist_ok=True)
    os.makedirs(ERROR_MAP_DIR, exist_ok=True)

    dataset = SingleFolderDataset(DATA_ROOT, scale=SCALE)
    loader = DataLoader(dataset, batch_size=BATCH_SIZE, shuffle=True, num_workers=2)

    model = HAT(scale=SCALE).to(DEVICE)
    optimizer = optim.Adam(model.parameters(), lr=LR)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=EPOCHS, eta_min=1e-6)
    l1_loss = nn.L1Loss()
    edge_loss = EdgeLoss().to(DEVICE)   # <-- ADD .to(DEVICE)

    best_psnr = 0.0
    for epoch in range(1, EPOCHS+1):
        model.train()
        epoch_loss = 0.0
        pbar = tqdm(loader, desc=f"Epoch {epoch}/{EPOCHS}")
        for lr, hr in pbar:
            lr, hr = lr.to(DEVICE), hr.to(DEVICE)
            sr = model(lr)
            loss_l1 = l1_loss(sr, hr)
            if USE_EDGE_LOSS:
                loss_edge = edge_loss(sr, hr)
                loss = loss_l1 + EDGE_LOSS_WEIGHT * loss_edge
            else:
                loss = loss_l1
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()
            epoch_loss += loss.item()
            pbar.set_postfix({"Loss": f"{loss.item():.4f}"})
        scheduler.step()

        if epoch % 5 == 0 or epoch == EPOCHS:
            psnr, ssim = validate(model, dataset)
            print(f"Epoch {epoch}: Val PSNR = {psnr:.2f} dB, SSIM = {ssim:.4f}")
            if psnr > best_psnr:
                best_psnr = psnr
                torch.save(model.state_dict(), os.path.join(SAVE_DIR, "best_hat.pth"))
                print("  -> Best model saved.")

    print(f"Training completed. Best PSNR: {best_psnr:.2f} dB")

def validate(model, dataset, save_error_maps=True):
    model.eval()
    psnr_list = []
    ssim_list = []
    with torch.no_grad():
        for idx in range(len(dataset)):
            lr, hr = dataset[idx]
            lr = lr.unsqueeze(0).to(DEVICE)
            hr = hr.unsqueeze(0).to(DEVICE)
            sr = model(lr)
            sr_np = sr.squeeze(0).cpu().numpy().transpose(1,2,0)
            hr_np = hr.squeeze(0).cpu().numpy().transpose(1,2,0)
            sr_np = np.clip(sr_np, 0, 1)
            hr_np = np.clip(hr_np, 0, 1)
            psnr = peak_signal_noise_ratio(hr_np, sr_np, data_range=1)
            ssim = structural_similarity(hr_np, sr_np, multichannel=True, channel_axis=2, data_range=1)
            psnr_list.append(psnr)
            ssim_list.append(ssim)

            if save_error_maps:
                error = np.abs(sr_np - hr_np).mean(axis=2)
                plt.figure(figsize=(6,5))
                plt.imshow(error, cmap='hot', vmin=0, vmax=0.1)
                plt.colorbar(label='Absolute Error')
                plt.title(f'Error Map - Image {idx}')
                plt.axis('off')
                plt.savefig(os.path.join(ERROR_MAP_DIR, f"error_{idx}.png"), bbox_inches='tight', dpi=150)
                plt.close()
    return np.mean(psnr_list), np.mean(ssim_list)

if __name__ == "__main__":
    train()